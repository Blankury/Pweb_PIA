
function sumar(){
    var op1 = parseFloat(document.getElementById("op1").value);
    var op2 = parseFloat(document.getElementById("op2").value);
    alert(op1 + op2) ;
}
function restar(){
    var op1 = parseFloat(document.getElementById("op1").value);
    var op2 = parseFloat(document.getElementById("op2").value);
    alert(op1 - op2) ;
}


function dividir(){
    var op1 = parseFloat(document.getElementById("op1").value);
    var op2  = parseFloat(document.getElementById("op2").value);
    alert(op1 / op2);
}
function multiplicar(){
    var op1 = parseFloat(document.getElementById("op1").value);
    var op2  = parseFloat(document.getElementById("op2").value);
    alert(op1 * op2);
}